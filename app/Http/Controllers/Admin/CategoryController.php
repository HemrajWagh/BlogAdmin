    {
        // return $request->all();
        $this->validate($request,[
            'name'=>'required',
            'slug'=>'required',
        ]);

        $categories = new category;
        $categories->name= $request->name;
        $categories->slug= $request->slug;
        $categories->save();
        return redirect(route('category.index'));
    }

